UINT8 ucSysClkChange(UINT8 CLK)
{
    switch(CLK%4)
	{
	     case 0:
			 vScu_SetSysClk32K();
			 
			 break;
		 case 1:
		     vScu_SetSysClk64K();
			 
			 break;
		 case 2:
			 
			 vScu_SetSysClkOsc500K();
			 break;
		 case 3:
		     
			 vScu_SetSysClkOsc2M();
			 
			 break;
		 default:
		     break;
	}	
	
	return 1;
}

